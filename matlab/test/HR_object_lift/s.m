%% ======================================

v1 = cross(v_rot, r);
v1 = v1(1:2);
v2 = [-r(2); r(1)] * v_rot(3);
v_err = v1-v2


%% ======================================

t1 = cross(r, F);
t1 = t1(3);
t2 = [-r(2); r(1)]' * F(1:2);
t_err = t1-t2