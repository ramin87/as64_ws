v_rot = [0 0 rand()]';

r = [rand() rand() 0]';

F = [rand() rand() 0]';

%% ======================================

a1 = cross(v_rot, cross(v_rot,r) );
a1 = a1(1:2);
a2 = -r(1:2)*v_rot(3)^2;
